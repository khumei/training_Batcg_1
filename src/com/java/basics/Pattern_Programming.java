package com.java.basics;

public class Pattern_Programming {

	public static void main(String[] args) {
		for (int i = 1; i<=6; i++) {
			System.out.println();
			for (int j =1; j<=i;j++) {
				System.out.print("8 ");
			}
		}
		for (int a = 1; a<=5; a++) {
			System.out.println();
			for (int b =5; b>=a;b--) {
				System.out.print("8 ");
			}
		}
	}

}
